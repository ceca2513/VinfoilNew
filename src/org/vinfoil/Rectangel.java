package org.vinfoil;

import java.util.*;

public class Rectangel {

	private float X11;
	private float Y11;
	private float X12;
	private float Y12;
	private float A;
	private float B;
    
    
    public Rectangel(){
    	
    }
    public float getX11(){
    	return X11;
    }
    public void setX11(float LC){
    	X11=LC;
    }
    public float getX12(){
    	return X12;
    }
    public void setX12(float RC){
    	X12=RC;
    }
    public float getY11(){
    	return Y11;
    }
    public void setY11(float TC){
    	Y11=TC;
    }
    public float getY12(){
    	return Y12;
    }
    public void setY12(float BC){
    	Y12=BC;
    }
    public float getA(){
    	return A;
    }
    public void setA(float LC){
    	A=LC;
    }
    public float getB(){
    	return B;
    }
    public void setB(float LC){
    	B=LC;
    }
  
}

package org.vinfoil;

import java.util.*;

public class PrintingSheet {
	
 private int width;
 private int length;
 private float CPC;
 
 public PrintingSheet(){
	 
 }
 public int getWidth(){
	 return width;
 }
 public void setWidth(int W){
	 width=W;
 }
 public int getLength(){
	 return length;
 }
 public void setLength(int L){
	 length=L;
 }
 public float getCPC(){
	 return CPC;
 }
 public void setCPC(float Cir){
	 CPC=Cir;
 }
 
}

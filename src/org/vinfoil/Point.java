package org.vinfoil;

public class Point {
	
	private float X;
	private float Y;
	
	public float getX(){
    	return X;
    }
    public void setX(float Top){
    	X=Top;
    }
    public float getY(){
    	return Y;
    }
    public void setY(float Top){
    	Y=Top;
    }
    public Point(int Xi, int Yj){
    	X=Xi;
    	Y=Yj;
    	
    }

}